#include "libft.h"

void	ft_bzero(void *s, int nbyte)
{
	size_t			i;
	unsigned char	*p;

	i = (size_t) nbyte;
	p = s;
	while (i > 0)
	{
		*p++ = 0;
		i--;
	}
}
